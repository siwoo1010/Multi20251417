#include <stdio.h>

int main() {
    int i, j;  // 반복문에서 사용할 변수 i와 j 선언

    // 5줄 출력 (0부터 4까지 총 5회 반복)
    for (i = 0; i < 5; i++) {
        // 한 줄에 6글자 출력 (0부터 5까지 총 6회 반복)
        for (j = 0; j < 6; j++) {
            // 만약 현재 줄(i)이 첫 번째(0) 또는 마지막(4) 줄이거나
            // 현재 칸(j)이 첫 번째(0) 또는 마지막(5) 칸이라면
            // 해당 위치에 '#' 문자 출력 (테두리 역할)
            if (i == 0 || i == 4 || j == 0 || j == 5)
                putchar('#');
            else
                // 위 조건이 아니면 내부 공간이므로 공백 출력
                putchar(' ');
        }
        // 한 줄 출력이 끝나면 줄바꿈 문자 출력해서 다음 줄로 이동
        putchar('\n');
    }

    return 0;  // 프로그램 정상 종료
}
